    ADD COLUMN seq_re2 INTEGER NOT NULL DEFAULT 0,
    ADD COLUMN cod_clp VARCHAR(64),
    ADD COLUMN cod_st1 VARCHAR(32),
    ADD COLUMN cod_und VARCHAR(16),
    ADD COLUMN vlu_re2 NUMERIC(19, 4),
    ADD COLUMN dsc_re2 NUMERIC(19, 4),
    ADD COLUMN dsr_re2 NUMERIC(19, 4),
    ADD COLUMN icms_aliquota NUMERIC(19, 4),
    ADD COLUMN icms_base NUMERIC(19, 4),
    ADD COLUMN icms_valor NUMERIC(19, 4),
    ADD COLUMN icms_reducao_base NUMERIC(19, 4),
    ADD COLUMN icms_substituicao_base NUMERIC(19, 4),
    ADD COLUMN icms_substituicao_valor NUMERIC(19, 4),
    ADD COLUMN icms_substituicao_aliquota NUMERIC(19, 4),
    ADD COLUMN icms_substituicao_margem NUMERIC(19, 4),
    ADD COLUMN icms_substituicao_reducao_base NUMERIC(19, 4),
    ADD COLUMN ipi_aliquota NUMERIC(19, 4),
    ADD COLUMN ipi_base NUMERIC(19, 4),
    ADD COLUMN ipi_valor NUMERIC(19, 4),
    ADD COLUMN ipi_classificacao VARCHAR(32),
    ADD COLUMN ipi_cst VARCHAR(8),
    ADD COLUMN pis_base NUMERIC(19, 4),
    ADD COLUMN pis_aliquota NUMERIC(19, 4),
    ADD COLUMN pis_valor NUMERIC(19, 4),
    ADD COLUMN pis_cst VARCHAR(8),
    ADD COLUMN cofins_base NUMERIC(19, 4),
