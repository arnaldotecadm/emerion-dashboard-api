    ADD COLUMN cofins_valor NUMERIC(19, 4),
    ADD COLUMN cofins_cst VARCHAR(8),
    ADD COLUMN desconto_valor NUMERIC(19, 4),
    ADD COLUMN frete_valor NUMERIC(19, 4),
    ADD COLUMN seguro_valor NUMERIC(19, 4),
    ADD COLUMN outras_despesas_valor NUMERIC(19, 4),
    ADD COLUMN total_item_tributado NUMERIC(19, 4),
    ADD COLUMN tot_ren NUMERIC(19, 4),
    ADD COLUMN tot_ge2 NUMERIC(19, 4),
    ADD COLUMN observacao VARCHAR(500),
    ADD COLUMN pedido_compra_cliente VARCHAR(64),
    ADD COLUMN item_pedido_compra_cliente INTEGER,
    ADD COLUMN nro_re2 INTEGER,
    ADD COLUMN flg_val VARCHAR(4),
    ADD COLUMN flg_pac VARCHAR(4),
    ADD COLUMN flg_lib VARCHAR(4),
    ADD COLUMN cod_cfo VARCHAR(16);

ALTER TABLE customer_order_item ALTER COLUMN seq_re2 DROP DEFAULT;
