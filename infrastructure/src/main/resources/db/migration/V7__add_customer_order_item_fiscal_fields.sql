-- Adds the additional PEDRE2 fields now exposed by emerion-load-service's
-- CustomerOrderItemProjection (tax/fiscal breakdown, discounts, and legacy
-- tracking flags for each order line item). All columns are nullable
-- except seq_re2, which every source row is expected to carry.
